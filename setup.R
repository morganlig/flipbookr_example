# Installation
# Must install to see templates when creating a new RMarkdown

# install devtools to install from github
install.packages("devtools")

# install the development version of flipbookr with devtools
devtools::install_github("EvaMaeRey/flipbookr")

# alternatively, install flipbookr with remotes
remotes::install_github("EvaMaeRey/flipbookr")

# Use this package with the rmarkdown presentation tool, xaringan
install.packages("xaringan")
